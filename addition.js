document.getElementById("changetxt").addEventListener("click", colourmod);
// defining the click as action

function colourmod() {
  document.getElementById("changetxt").style.color = "#cfd3a5";
  document.getElementById("txt").innerText =
    "Thank you for subscribing, expect to hear from us shortly!";
  // adding content to the empty text zone^
  document.getElementById("txt").innerHTML;
  txt.style.color = "#7c7777";
  // changing text colour of the subscribe button
}
